/*
MINORANTE
*/

minorante(L, Min) :- 
    armazensEntregas(L),
    minoranteArm(L, Min).

armazensEntregas(L) :- findall(AD, entrega(_, _, _, AD, _, _), L).
	
%se so existir um elemento na lista entao o menor é o tempo desse trajeto de AO a X       
menorTempo(AO,[X], TT):- 
    dadosCam_t_e_ta(_,AO,X,T,_,_),
    entrega(_, _, Carga, AO,_,TD),
    TT is TD + T*(7500+Carga)/11800.
%ver qual o menor entre X e Y
menorTempo(AO,[X,Y|Rest], Min) :-
    entrega(_, _, Carga, AO, _, TD),
    dadosCam_t_e_ta(_,X,AO,TX,_,_),
	dadosCam_t_e_ta(_,Y,AO,TY,_,_),
    TTX is TD + TX*(7500+Carga)/11800,
    TTY is TD + TY*(7500+Carga)/11800,
   	TTX =< TTY, menorTempo(AO,[X|Rest], Min).
menorTempo(AO,[X,Y|Rest], Min) :-
    entrega(_, _, Carga, AO, _, TD),
    dadosCam_t_e_ta(_,X,AO,TX,_,_),
	dadosCam_t_e_ta(_,Y,AO,TY,_,_),
    TTX is TD + TX*(7500+Carga)/11800,
    TTY is TD + TY*(7500+Carga)/11800,
	TTY < TTX, menorTempo(AO,[Y|Rest], Min).

minoranteArm([_],Min):- Min is 0.
minoranteArm([A|L],Min):- 
    menorTempo(A, L, Menor),
